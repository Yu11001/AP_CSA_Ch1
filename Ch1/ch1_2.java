package Ch1;

/*
 * (Display five messages) Write a program that displays Welcome to Java five times.
 */
public class ch1_2 {
    public static void main(String[] args) {
        int i;
        for (i = 0; i < 5; i++) {
            System.out.println("Welcome to Java");
        }
    }
}

